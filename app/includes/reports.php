<?php

declare(strict_types=1);

function carceris_report_facility_name(): string
{
    return setting('facility_name', 'Correctional Facility') ?: 'Correctional Facility';
}

function carceris_report_title(array $logDay): string
{
    return carceris_print_log_title();
}

function carceris_report_subject(array $logDay): string
{
    return carceris_report_title($logDay) . ' - ' . format_log_date($logDay['operational_date']);
}

function carceris_report_entry_count_label(array $entries): string
{
    $count = count($entries);

    return $count . ' ' . ($count === 1 ? 'entry' : 'entries');
}

function carceris_render_log_report_plain_text(array $logDay, array $entries): string
{
    $lines = [];

    $lines[] = carceris_report_title($logDay);
    $lines[] = str_repeat('=', strlen(carceris_report_title($logDay)));
    $lines[] = 'Operational Period: ' . $logDay['log_label'];
    $lines[] = 'Operational Date: ' . format_log_date($logDay['operational_date']);
    $lines[] = 'Generated: ' . carceris_format_datetime(new DateTimeImmutable('now'));
    $lines[] = 'Generated By: Carceris';
    $lines[] = 'Entry Count: ' . carceris_report_entry_count_label($entries);
    $lines[] = '';
    $lines[] = 'Entries';
    $lines[] = '-------';

    if (!$entries) {
        $lines[] = 'No log entries were recorded for this operational period.';
        return implode(PHP_EOL, $lines) . PHP_EOL;
    }

    foreach ($entries as $entry) {
        $parts = [
            format_log_time($entry['event_time']),
            $entry['category'] ?? '',
            ucfirst((string) ($entry['priority'] ?? 'normal')),
            'Status: ' . entry_status_label($entry),
        ];

        if (!empty($entry['location'])) {
            $parts[] = 'Location: ' . $entry['location'];
        }

        if (!empty($entry['inmate_name'])) {
            $parts[] = 'Inmate: ' . $entry['inmate_name'];
        }

        if (!empty($entry['created_by_name'])) {
            $parts[] = 'Officer: ' . $entry['created_by_name'];
        }

        $lines[] = implode(' | ', $parts);
        $lines[] = trim((string) ($entry['entry_text'] ?? ''));

        $lateNote = entry_late_note($entry);

        if ($lateNote !== '') {
            $lines[] = $lateNote;
        }

        $statusNote = entry_status_note($entry);

        if ($statusNote !== '') {
            $lines[] = $statusNote;
        }

        $lines[] = '';
    }

    return implode(PHP_EOL, $lines);
}

