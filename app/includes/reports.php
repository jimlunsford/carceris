function carceris_report_download_filename(array $logDay, string $format): string
{
    $extension = match ($format) {
        'html' => 'html',
        'pdf' => 'pdf',
        default => 'txt',
    };

    return carceris_daily_log_filename_base($logDay) . '.' . $extension;
}

function carceris_record_report_generation(
    array $logDay,
    string $format,
    array $user,
    string $status = 'generated'
): int {
    $bodyFormat = match ($format) {
        'html' => 'html',
        'pdf' => 'none',
        default => 'plain_text',
    };

    $attachmentFormat = $format === 'pdf' ? 'pdf' : 'none';

    $stmt = db()->prepare(
        'INSERT INTO report_downloads
            (log_day_id, body_format, attachment_format, subject, status, triggered_by, created_at)
         VALUES
            (:log_day_id, :body_format, :attachment_format, :subject, :status, :triggered_by, NOW())'
    );

    $stmt->execute([
        'log_day_id' => (int) $logDay['id'],
        'body_format' => $bodyFormat,
        'attachment_format' => $attachmentFormat,
        'subject' => carceris_report_subject($logDay),
        'status' => $status === 'failed' ? 'failed' : 'generated',
        'triggered_by' => (int) $user['id'],
    ]);

    return (int) db()->lastInsertId();
}


function carceris_report_delivery_already_sent(int $logDayId): bool
{
    $stmt = db()->prepare(
        'SELECT COUNT(*)
         FROM report_email_deliveries
         WHERE log_day_id = :log_day_id
           AND delivery_type = "automatic"
           AND status = "sent"'
    );
    $stmt->execute(['log_day_id' => $logDayId]);

    if ((int) $stmt->fetchColumn() > 0) {
        return true;
    }

    if (carceris_schema_table_exists('report_deliveries')) {
        $legacy = db()->prepare(
            'SELECT COUNT(*)
             FROM report_deliveries
             WHERE log_day_id = :log_day_id
               AND delivery_type = "automatic"
               AND status = "sent"'
        );
        $legacy->execute(['log_day_id' => $logDayId]);

        return (int) $legacy->fetchColumn() > 0;
    }

    return false;
}

function carceris_report_attachment_for_format(array $logDay, array $entries, string $format): ?array
{
    return match ($format) {
        'none' => null,
        'plain_text' => [
            'filename' => carceris_report_download_filename($logDay, 'text'),
            'content_type' => 'text/plain; charset=UTF-8',
            'content' => carceris_render_log_report_plain_text($logDay, $entries),
        ],
        'html' => [
            'filename' => carceris_report_download_filename($logDay, 'html'),
            'content_type' => 'text/html; charset=UTF-8',
            'content' => carceris_render_log_report_html($logDay, $entries),
        ],
        'pdf' => [
            'filename' => carceris_report_download_filename($logDay, 'pdf'),
            'content_type' => 'application/pdf',
            'content' => carceris_render_log_report_pdf($logDay, $entries),
        ],
        default => throw new RuntimeException('Unsupported attachment format: ' . $format),
    };
}

function carceris_report_body_for_format(array $logDay, array $entries, string $format, string $attachmentFormat): array
{
    if ($format === 'plain_text') {
        return [
            'body_format' => 'plain_text',
            'body' => carceris_render_log_report_plain_text($logDay, $entries),
        ];
    }

    if ($format === 'html') {
        return [
            'body_format' => 'html',
            'body' => carceris_render_log_report_html($logDay, $entries),
        ];
    }

    if ($format === 'none') {
        if ($attachmentFormat === 'none') {
            throw new RuntimeException('Report body and attachment cannot both be set to None.');
        }

        return [
            'body_format' => 'plain_text',
            'body' => "The Carceris daily log report is attached.\n\nOperational Period: " . $logDay['log_label'] . "\n",
        ];
    }

    throw new RuntimeException('Unsupported report body format: ' . $format);
}

function carceris_record_report_send_delivery(
    array $logDay,
    array $settings,
    array $message,
    ?array $triggeredBy,
    string $deliveryType,
    string $status,
    ?string $errorMessage = null
): int {
    $stmt = db()->prepare(
        'INSERT INTO report_email_deliveries
            (log_day_id, delivery_type, transport, body_format, attachment_format, recipient_to, recipient_cc, recipient_bcc, subject, status, error_message, sent_at, triggered_by, created_at)
         VALUES
            (:log_day_id, :delivery_type, :transport, :body_format, :attachment_format, :recipient_to, :recipient_cc, :recipient_bcc, :subject, :status, :error_message, :sent_at, :triggered_by, NOW())'
    );

    $stmt->execute([
        'log_day_id' => (int) $logDay['id'],
        'delivery_type' => $deliveryType === 'automatic' ? 'automatic' : 'manual',
        'transport' => (string) ($settings['report_mail_transport'] ?? 'manual_only'),
        'body_format' => (string) ($settings['report_body_format'] ?? 'plain_text'),
        'attachment_format' => (string) ($settings['report_attachment_format'] ?? 'none'),
        'recipient_to' => implode(', ', $message['to'] ?? []),
        'recipient_cc' => implode(', ', $message['cc'] ?? []),
        'recipient_bcc' => implode(', ', $message['bcc'] ?? []),
        'subject' => (string) ($message['subject'] ?? carceris_report_subject($logDay)),
        'status' => in_array($status, ['pending', 'sent', 'failed', 'skipped'], true) ? $status : 'pending',
        'error_message' => $errorMessage,
        'sent_at' => $status === 'sent' ? current_datetime() : null,
        'triggered_by' => $triggeredBy ? (int) $triggeredBy['id'] : null,
    ]);

    return (int) db()->lastInsertId();
}

function carceris_send_log_day_report(array $logDay, ?array $triggeredBy, string $deliveryType): array
{
    $settings = carceris_mail_settings();
    $entries = get_entries_for_log_day((int) $logDay['id']);

    $bodyFormatSetting = (string) ($settings['report_body_format'] ?? 'plain_text');
    $attachmentFormat = (string) ($settings['report_attachment_format'] ?? 'none');

    $bodyData = carceris_report_body_for_format($logDay, $entries, $bodyFormatSetting, $attachmentFormat);
    $attachment = carceris_report_attachment_for_format($logDay, $entries, $attachmentFormat);

    $message = carceris_mail_message_from_settings(
        $settings,
        carceris_report_subject($logDay),
        $bodyData['body'],
        $bodyData['body_format']
    );

    if ($attachment !== null) {
        $message['attachments'][] = $attachment;
    }

    try {
        carceris_mail_send($settings, $message);

        $deliveryId = carceris_record_report_send_delivery(
            $logDay,
            $settings,
            $message,
            $triggeredBy,
            $deliveryType,
            'sent'
        );

        audit_event(
            $deliveryType === 'automatic' ? 'report_automatic_sent' : 'report_manual_sent',
            $triggeredBy ? (int) $triggeredBy['id'] : null,
            $triggeredBy['username'] ?? null,
            'Sent completed daily log. Operational date: ' . $logDay['operational_date'] . '. Transport: ' . ($settings['report_mail_transport'] ?? 'manual_only') . '.',
            'report_email_delivery',
            $deliveryId
        );

        return [
            'status' => 'sent',
            'delivery_id' => $deliveryId,
            'log_day_id' => (int) $logDay['id'],
            'operational_date' => $logDay['operational_date'],
        ];
    } catch (Throwable $exception) {
        $deliveryId = carceris_record_report_send_delivery(
            $logDay,
            $settings,
            $message,
            $triggeredBy,
            $deliveryType,
            'failed',
            $exception->getMessage()
        );

        audit_event(
            $deliveryType === 'automatic' ? 'report_automatic_failed' : 'report_manual_failed',
            $triggeredBy ? (int) $triggeredBy['id'] : null,
            $triggeredBy['username'] ?? null,
            'Daily log send failed. Operational date: ' . $logDay['operational_date'] . '. Error: ' . $exception->getMessage(),
            'report_email_delivery',
            $deliveryId
        );

        throw $exception;
    }
}

function carceris_send_previous_log_report_now(?array $triggeredBy, string $deliveryType = 'manual'): array
{
    $logDay = get_or_create_previous_log_day($triggeredBy ? (int) $triggeredBy['id'] : null);

    return carceris_send_log_day_report($logDay, $triggeredBy, $deliveryType);
}

function carceris_send_log_day_report_by_id(int $logDayId, ?array $triggeredBy, string $deliveryType = 'manual'): array
{
    $logDay = carceris_log_day_by_id($logDayId);

    if (!$logDay) {
        throw new RuntimeException('Daily log not found.');
    }

    return carceris_send_log_day_report($logDay, $triggeredBy, $deliveryType);
}


function carceris_scheduled_report_due(): array
{
    $settings = carceris_mail_settings();

    if (($settings['report_delivery_enabled'] ?? '0') !== '1') {
        return [
            'due' => false,
            'reason' => 'Automatic log delivery is disabled.',
        ];
    }

    $sendTime = carceris_validate_report_send_time((string) ($settings['report_send_time'] ?? '05:00'));
    $now = new DateTimeImmutable('now');
    $scheduledToday = new DateTimeImmutable($now->format('Y-m-d') . ' ' . $sendTime);

    if ($now < $scheduledToday) {
        return [
            'due' => false,
            'reason' => 'Scheduled send time has not arrived yet.',
        ];
    }

    $logDay = get_or_create_previous_log_day(null);

    if (carceris_report_delivery_already_sent((int) $logDay['id'])) {
        return [
            'due' => false,
            'reason' => 'The previous completed log has already been sent automatically.',
            'log_day' => $logDay,
        ];
    }

    return [
        'due' => true,
        'reason' => 'Scheduled report is due.',
        'log_day' => $logDay,
    ];
}

function carceris_run_scheduled_daily_report(): array
{
    $due = carceris_scheduled_report_due();

    if (!$due['due']) {
        audit_event(
            'report_automatic_skipped',
            null,
            null,
            $due['reason'],
            isset($due['log_day']) ? 'log_day' : null,
            isset($due['log_day']) ? (int) $due['log_day']['id'] : null
        );

        return [
            'status' => 'skipped',
            'reason' => $due['reason'],
            'log_day' => $due['log_day'] ?? null,
        ];
    }

    return carceris_send_log_day_report($due['log_day'], null, 'automatic');
}
